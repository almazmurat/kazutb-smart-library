export function t(key, params = {}) {
  const value = key.split('.').reduce((acc, part) => acc?.[part], messages[spaLang]);
  const template = typeof value === 'string' ? value : key;

  return Object.entries(params).reduce(
    (result, [name, paramValue]) => result.replaceAll(`{${name}}`, String(paramValue)),
    template,
  );
}

export function withLang(path) {
  const url = new URL(path, window.location.origin);
  if (spaLang !== 'ru' && !url.searchParams.has('lang')) {
    url.searchParams.set('lang', spaLang);
  }
  return `${url.pathname}${url.search}`;
}

export function formatNumber(value) {
  const locale = spaLang === 'kk' ? 'kk-KZ' : spaLang === 'en' ? 'en-US' : 'ru-RU';
  return Number(value ?? 0).toLocaleString(locale);
}
